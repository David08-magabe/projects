countries = ("Tanzania","Kenya","Uganda","Rwanda","Burundi")

# indexing
print(countries[0])
print(countries[1])
print(countries[2])
print(countries[3])
print(countries[4])

# slicing
print(countries[:-1])

#to find how many times does the variable exist in the tuple
print(countries.count("Tanzania"))
print(countries.count("Kenya"))
print(countries.count("Uganda"))
print(countries.count("Rwanda"))
print(countries.count("Burundi"))

# to know the index of the variable in the tuple
print(countries.index("Tanzania"))
print(countries.index("Kenya"))
print(countries.index("Uganda"))
print(countries.index("Rwanda"))
print(countries.index("Burundi"))
